//移除图层函数
function removeLayer(){
	//获得当前选中的要移除的图层
	var selectedLayer = $("#layerTree").tree('getSelected');
	
	//首先，在allSeries中以及currentSeries中删除该图层的系列
	for(var i=0;i<allSeries.length;i++){
		if(allSeries[i].name==selectedLayer.id){
			allSeries.splice(i,1);
			break;
		}
	}
	if(selectedLayer.checked == true){//图层显示时才需要去currentSeries中删除相应图层系列
		for(var i=0;i<currentSeries.length;i++){
			if(currentSeries[i].name==selectedLayer.id){
				currentSeries.splice(i,1);
				break;
			}
		}
	}
	
	//并重新绘制地图
	option.series = currentSeries.slice(0);
	myChart.setOption(option,true);
	resetBmap();
	
	//然后，删除layersData中该图层的数据
	//layersData[selectedLayer.id] = null;
	for(var i = 0;i<layersData.length;i++){
		if(layersData[i].name == selectedLayer.id){
			layersData.splice(i,1);
			break;
		}
	}
	
	//最后，在layerTreeJson中删除该图层的相关信息,并更新图层树
	for(var i=0;i<layerTreeJson[0].children.length;i++){
		if(layerTreeJson[0].children[i].id==selectedLayer.id){
			layerTreeJson[0].children.splice(i,1);
			break;
		}
	}
	$("#layerTree").tree({
    	dataType:"json",
        data:layerTreeJson
    });
}


